package com.github.unjoinable.skyblock.statistics;

/**
 * Enum representing the various reasons for damage in the game.
 */
public enum DamageReason {
    /**
     * Damage caused by melee attacks.
     */
    MELEE,

    /**
     * Damage caused by ferocity.
     */
    FEROCITY,

    /**
     * Damage caused by abilities.
     */
    ABILITY,
}
